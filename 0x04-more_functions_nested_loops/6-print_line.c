#include "holberton.h"

/**
 * print line - print lines
 * @n: first parameter
 * Description: '_' is input dependent
 * Return: Returns nothing since function is void
 */

void print_line(int n)
{
int iteration;
{
for (iteration = 1; iteration <= n; iteration++)
{
_putchar('_');
}
_putchar('\n');
}
}
  
